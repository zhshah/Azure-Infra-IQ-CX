def _collect_hardware() -> str:
    return r'''# ═══════════════════════════════════════════════════════════════════════════════
# MODULE: Hardware & System Info
# ═══════════════════════════════════════════════════════════════════════════════
function Collect-HardwareInfo {
    param([string]$ComputerName)

    $cs = Get-WmiSafe -ClassName Win32_ComputerSystem -ComputerName $ComputerName
    $bios = Get-WmiSafe -ClassName Win32_BIOS -ComputerName $ComputerName
    $cpu = Get-WmiSafe -ClassName Win32_Processor -ComputerName $ComputerName | Select-Object -First 1

    $isVirtual = $false
    $hypervisor = ""
    if ($cs) {
        $mfg = ($cs.Manufacturer ?? "").ToLower()
        $mdl = ($cs.Model ?? "").ToLower()
        if ($mdl -match "virtual|vmware|kvm|xen|hyperv" -or $mfg -match "vmware|microsoft|xen|qemu") {
            $isVirtual = $true
            if ($mdl -match "vmware") { $hypervisor = "VMware" }
            elseif ($mdl -match "virtual" -or $mfg -match "microsoft") { $hypervisor = "Hyper-V" }
            elseif ($mdl -match "kvm" -or $mfg -match "qemu") { $hypervisor = "KVM" }
            else { $hypervisor = "Unknown" }
        }
    }

    return [PSCustomObject]@{
        Manufacturer       = $cs.Manufacturer ?? ""
        Model              = $cs.Model ?? ""
        SerialNumber       = $bios.SerialNumber ?? ""
        BIOSVersion        = $bios.SMBIOSBIOSVersion ?? ""
        TotalCores         = ($cpu.NumberOfCores ?? 0)
        LogicalProcessors  = ($cs.NumberOfLogicalProcessors ?? 0)
        TotalMemoryGB      = [math]::Round(($cs.TotalPhysicalMemory ?? 0) / 1GB, 1)
        CPUModel           = $cpu.Name ?? ""
        CPUSpeedGHz        = [math]::Round(($cpu.MaxClockSpeed ?? 0) / 1000, 2)
        IsVirtual          = $isVirtual
        HypervisorType     = $hypervisor
    }
}'''


def _collect_os() -> str:
    return r'''# ═══════════════════════════════════════════════════════════════════════════════
# MODULE: Operating System
# ═══════════════════════════════════════════════════════════════════════════════
function Collect-OSInfo {
    param([string]$ComputerName)

    $os = Get-WmiSafe -ClassName Win32_OperatingSystem -ComputerName $ComputerName

    $lastBoot = $null
    $uptimeDays = 0
    if ($os.LastBootUpTime) {
        try {
            $lastBoot = if ($os.LastBootUpTime -is [datetime]) { $os.LastBootUpTime }
                        else { [Management.ManagementDateTimeConverter]::ToDateTime($os.LastBootUpTime) }
            $uptimeDays = [math]::Round(((Get-Date) - $lastBoot).TotalDays, 0)
        } catch { $uptimeDays = 0 }
    }

    return [PSCustomObject]@{
        OSName          = $os.Caption ?? ""
        OSVersion       = $os.Version ?? ""
        OSBuild         = $os.BuildNumber ?? ""
        OSArchitecture  = $os.OSArchitecture ?? ""
        InstallDate     = try { $os.InstallDate.ToString("yyyy-MM-dd") } catch { "" }
        LastBootTime    = try { $lastBoot.ToString("yyyy-MM-dd HH:mm:ss") } catch { "" }
        UptimeDays      = $uptimeDays
    }
}'''


def _collect_applications() -> str:
    return r'''# ═══════════════════════════════════════════════════════════════════════════════
# MODULE: Installed Applications
# ═══════════════════════════════════════════════════════════════════════════════
function Collect-Applications {
    param([string]$ComputerName)

    $regPaths = @(
        "HKLM:\SOFTWARE\Microsoft\Windows\CurrentVersion\Uninstall\*",
        "HKLM:\SOFTWARE\WOW6432Node\Microsoft\Windows\CurrentVersion\Uninstall\*"
    )
    $apps = @()
    if ($ComputerName -eq "localhost" -or $ComputerName -eq $env:COMPUTERNAME) {
        foreach ($path in $regPaths) {
            $apps += Get-ItemProperty $path -ErrorAction SilentlyContinue |
                Where-Object { $_.DisplayName -and $_.DisplayName.Trim() -ne "" } |
                Select-Object @{N="Hostname";E={$ComputerName}},
                    @{N="Name";E={$_.DisplayName}},
                    @{N="Version";E={$_.DisplayVersion}},
                    @{N="Publisher";E={$_.Publisher}},
                    @{N="InstallDate";E={$_.InstallDate}}
        }
    } else {
        $apps = Invoke-Command -ComputerName $ComputerName -ErrorAction Stop -ScriptBlock {
            $paths = @(
                "HKLM:\SOFTWARE\Microsoft\Windows\CurrentVersion\Uninstall\*",
                "HKLM:\SOFTWARE\WOW6432Node\Microsoft\Windows\CurrentVersion\Uninstall\*"
            )
            $result = @()
            foreach ($p in $paths) {
                $result += Get-ItemProperty $p -ErrorAction SilentlyContinue |
                    Where-Object { $_.DisplayName -and $_.DisplayName.Trim() -ne "" } |
                    Select-Object @{N="Name";E={$_.DisplayName}},
                        @{N="Version";E={$_.DisplayVersion}},
                        @{N="Publisher";E={$_.Publisher}},
                        @{N="InstallDate";E={$_.InstallDate}}
            }
            $result
        } | Select-Object @{N="Hostname";E={$ComputerName}}, Name, Version, Publisher, InstallDate
    }
    return $apps
}'''


