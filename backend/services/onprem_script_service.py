def _header(opt: dict) -> str:
    return '''<#
.SYNOPSIS
    Azure Infrastructure Optimizer — On-Premises Server Data Collection Script
.DESCRIPTION
    Collects comprehensive server inventory data for migration assessment,
    BCDR planning, and hybrid cloud analysis. Generated by the Azure
    Infrastructure Optimization Platform.
.NOTES
    Script Version: 1.0.0
    Generated:      $(Get-Date -Format "yyyy-MM-dd HH:mm:ss")
    Requires:       PowerShell 5.1+ / PowerShell 7+
    Run As:         Administrator (recommended for full data collection)
#>

#Requires -Version 5.1
$ErrorActionPreference = "Continue"
$ProgressPreference    = "SilentlyContinue"
$ScriptVersion         = "1.0.0"
$CollectionTimestamp    = Get-Date -Format "yyyy-MM-dd_HHmmss"'''


def _parameters_block(opt: dict) -> str:
    server_list_default = ""
    if opt["target_scope"] == "custom_list" and opt["custom_server_list"]:
        escaped = '","'.join(opt["custom_server_list"])
        server_list_default = f'        [string[]]$ServerList = @("{escaped}"),'
    elif opt["target_scope"] == "localhost":
        server_list_default = '        [string[]]$ServerList = @($env:COMPUTERNAME),'
    else:
        server_list_default = '        [string[]]$ServerList = @(),'

    return f'''[CmdletBinding()]
param(
{server_list_default}
        [string]$OutputPath = ".\\OnPrem_Collection_$CollectionTimestamp",
        [int]$MaxConcurrent = {opt["max_concurrent"]},
        [int]$TimeoutSeconds = {opt["timeout_per_server"]},
        [switch]$SkipCompression
)'''


def _helper_functions() -> str:
    return r'''# ═══════════════════════════════════════════════════════════════════════════════
# HELPER FUNCTIONS
# ═══════════════════════════════════════════════════════════════════════════════

function Write-Status {
    param([string]$Message, [string]$Level = "INFO")
    $ts = Get-Date -Format "HH:mm:ss"
    $color = switch ($Level) {
        "ERROR"   { "Red" }
        "WARN"    { "Yellow" }
        "SUCCESS" { "Green" }
        default   { "Cyan" }
    }
    Write-Host "[$ts] [$Level] $Message" -ForegroundColor $color
}

function Invoke-SafeCommand {
    <#
    .SYNOPSIS
        Execute a script block with error handling; returns $null on failure.
    #>
    param(
        [ScriptBlock]$Command,
        [string]$Description = "command",
        [string]$Hostname = "localhost"
    )
    try {
        $result = & $Command
        return $result
    } catch {
        Write-Status "[$Hostname] Failed: $Description — $($_.Exception.Message)" -Level "WARN"
        return $null
    }
}

function Get-WmiSafe {
    <#
    .SYNOPSIS
        Try CIM first, fall back to WMI for older systems.
    #>
    param(
        [string]$ClassName,
        [string]$ComputerName = "localhost",
        [string]$Namespace = "root\cimv2"
    )
    try {
        if ($ComputerName -eq "localhost" -or $ComputerName -eq $env:COMPUTERNAME) {
            return Get-CimInstance -ClassName $ClassName -Namespace $Namespace -ErrorAction Stop
        }
        return Get-CimInstance -ClassName $ClassName -Namespace $Namespace -ComputerName $ComputerName -ErrorAction Stop
    } catch {
        try {
            return Get-WmiObject -Class $ClassName -Namespace $Namespace -ComputerName $ComputerName -ErrorAction Stop
        } catch {
            Write-Status "[$ComputerName] CIM/WMI fallback failed for $ClassName — $($_.Exception.Message)" -Level "WARN"
            return $null
        }
    }
}'''


def _server_resolution(opt: dict) -> str:
    if opt["target_scope"] == "domain":
        ou_filter = ""
        if opt["domain_ou_filter"]:
            ou_filter = f' -SearchBase "{opt["domain_ou_filter"]}"'
        return f'''# ═══════════════════════════════════════════════════════════════════════════════
# SERVER RESOLUTION — Active Directory Discovery
# ═══════════════════════════════════════════════════════════════════════════════
if (-not $ServerList -or $ServerList.Count -eq 0) {{
    Write-Status "Discovering servers from Active Directory..."
    try {{
        Import-Module ActiveDirectory -ErrorAction Stop
        $adComputers = Get-ADComputer -Filter 'OperatingSystem -like "*Server*"'{ou_filter} -Properties OperatingSystem,LastLogonDate |
            Where-Object {{ $_.Enabled -eq $true -and $_.LastLogonDate -gt (Get-Date).AddDays(-90) }}
        $ServerList = $adComputers | Select-Object -ExpandProperty DNSHostName
        Write-Status "Found $($ServerList.Count) servers in AD" -Level "SUCCESS"
    }} catch {{
        Write-Status "AD discovery failed: $($_.Exception.Message). Falling back to localhost." -Level "ERROR"
        $ServerList = @($env:COMPUTERNAME)
    }}
}}'''
    else:
        return '''# ═══════════════════════════════════════════════════════════════════════════════
# SERVER RESOLUTION
# ═══════════════════════════════════════════════════════════════════════════════
if (-not $ServerList -or $ServerList.Count -eq 0) {
    $ServerList = @($env:COMPUTERNAME)
}
Write-Status "Target servers: $($ServerList -join ', ')"'''


