def _collect_sql() -> str:
    return r'''# ═══════════════════════════════════════════════════════════════════════════════
# MODULE: SQL Server Discovery
# ═══════════════════════════════════════════════════════════════════════════════
function Collect-SqlInstances {
    param([string]$ComputerName)

    $instances = @()
    $databases = @()

    # Discover SQL instances via registry
    $sqlServices = Invoke-SafeCommand -Description "SQL Services" -Hostname $ComputerName -Command {
        if ($ComputerName -eq "localhost" -or $ComputerName -eq $env:COMPUTERNAME) {
            Get-Service | Where-Object { $_.Name -match "^MSSQL\$|^MSSQLSERVER$" -and $_.Status -eq "Running" }
        } else {
            Get-Service -ComputerName $ComputerName | Where-Object { $_.Name -match "^MSSQL\$|^MSSQLSERVER$" -and $_.Status -eq "Running" }
        }
    }

    if (-not $sqlServices) { return @{ Instances = @(); Databases = @() } }

    foreach ($svc in $sqlServices) {
        $instName = if ($svc.Name -eq "MSSQLSERVER") { "MSSQLSERVER" } else { $svc.Name -replace "^MSSQL\$", "" }
        $connStr  = if ($instName -eq "MSSQLSERVER") { $ComputerName } else { "$ComputerName\$instName" }

        $sqlInfo = Invoke-SafeCommand -Description "SQL Info ($instName)" -Hostname $ComputerName -Command {
            $conn = New-Object System.Data.SqlClient.SqlConnection("Server=$connStr;Integrated Security=True;Connection Timeout=10")
            $conn.Open()
            $cmd = $conn.CreateCommand()
            $cmd.CommandText = "SELECT SERVERPROPERTY('ProductVersion') AS Version, SERVERPROPERTY('Edition') AS Edition, SERVERPROPERTY('ProductLevel') AS SP, SERVERPROPERTY('Collation') AS Collation"
            $reader = $cmd.ExecuteReader()
            $reader.Read() | Out-Null
            $info = [PSCustomObject]@{
                Version   = $reader["Version"]
                Edition   = $reader["Edition"]
                SP        = $reader["SP"]
                Collation = $reader["Collation"]
            }
            $reader.Close()

            # Get databases
            $cmd2 = $conn.CreateCommand()
            $cmd2.CommandText = @"
SELECT d.name, CAST(SUM(mf.size) * 8.0 / 1024 AS DECIMAL(18,1)) AS SizeMB,
       d.recovery_model_desc, d.compatibility_level, d.state_desc,
       (SELECT MAX(bs.backup_finish_date) FROM msdb.dbo.backupset bs WHERE bs.database_name = d.name) AS LastBackup
FROM sys.databases d
LEFT JOIN sys.master_files mf ON d.database_id = mf.database_id
WHERE d.database_id > 4
GROUP BY d.name, d.recovery_model_desc, d.compatibility_level, d.state_desc
"@
            $dbs = @()
            $reader2 = $cmd2.ExecuteReader()
            while ($reader2.Read()) {
                $dbs += [PSCustomObject]@{
                    DatabaseName  = $reader2["name"]
                    SizeMB        = [math]::Round($reader2["SizeMB"], 1)
                    RecoveryModel = $reader2["recovery_model_desc"]
                    CompatLevel   = $reader2["compatibility_level"]
                    State         = $reader2["state_desc"]
                    LastBackup    = if ($reader2["LastBackup"] -ne [DBNull]::Value) { $reader2["LastBackup"].ToString("yyyy-MM-dd HH:mm") } else { "" }
                }
            }
            $reader2.Close()
            $conn.Close()

            [PSCustomObject]@{ Info = $info; Databases = $dbs }
        }

        if ($sqlInfo) {
            $instances += [PSCustomObject]@{
                Hostname     = $ComputerName
                InstanceName = $instName
                Version      = $sqlInfo.Info.Version
                Edition      = $sqlInfo.Info.Edition
                ServicePack  = $sqlInfo.Info.SP
                Collation    = $sqlInfo.Info.Collation
                TCPPort      = 1433
                MaxMemoryMB  = 0
                MaxDOP       = 0
            }
            foreach ($db in $sqlInfo.Databases) {
                $databases += [PSCustomObject]@{
                    Hostname      = $ComputerName
                    InstanceName  = $instName
                    DatabaseName  = $db.DatabaseName
                    SizeMB        = $db.SizeMB
                    RecoveryModel = $db.RecoveryModel
                    CompatLevel   = $db.CompatLevel
                    State         = $db.State
                    LastBackup    = $db.LastBackup
                }
            }
        }
    }

    return @{ Instances = $instances; Databases = $databases }
}'''


