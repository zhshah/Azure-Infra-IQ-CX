"""
On-Premises PowerShell Script Builder — generates collection scripts
based on user-selected options with robust error handling.
"""
from __future__ import annotations

from typing import Dict, List


def generate_script(options: dict) -> str:
    """Generate a PowerShell collection script based on the provided options."""
    opt = _defaults(options)
    sections = [
        _header(opt),
        _parameters_block(opt),
        _helper_functions(),
        _server_resolution(opt),
        _output_setup(opt),
    ]

    # Add collection modules based on options
    if opt["collect_hardware"]:
        sections.append(_collect_hardware())
    if opt["collect_os"]:
        sections.append(_collect_os())
    if opt["collect_applications"]:
        sections.append(_collect_applications())
    if opt["collect_services"]:
        sections.append(_collect_services())
    if opt["collect_sql"]:
        sections.append(_collect_sql())
    if opt["collect_iis"]:
        sections.append(_collect_iis())
    if opt["collect_security"]:
        sections.append(_collect_security())
    if opt["collect_certificates"]:
        sections.append(_collect_certificates())
    if opt["collect_performance"]:
        sections.append(_collect_performance())

    sections.append(_main_loop(opt))
    sections.append(_export_and_compress(opt))
    sections.append(_footer())

    return "\n\n".join(sections)


def _defaults(options: dict) -> dict:
    defaults = {
        "collect_hardware": True,
        "collect_os": True,
        "collect_applications": True,
        "collect_services": True,
        "collect_sql": True,
        "collect_iis": True,
        "collect_security": True,
        "collect_certificates": True,
        "collect_performance": True,
        "collect_clustering": False,
        "collect_hyperv": False,
        "target_scope": "localhost",
        "custom_server_list": [],
        "domain_ou_filter": "",
        "credential_method": "current",
        "output_format": "csv",
        "compress_output": True,
        "max_concurrent": 5,
        "timeout_per_server": 300,
    }
    defaults.update(options)
    return defaults


